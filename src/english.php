$lang['HELP_INSTRUCTIONS_TITLE'] = 'INSTRUCTIONS';
$lang['HELP_INSTRUCTIONS'] = '

<h2>General</h2>
<p>
In rugby, a scrum refers to the manner of restarting the game after a minor infraction. In the early 1990s, 
Ken Schwaber used what would become Scrum at his company, Advanced Development Methods, and Jeff Sutherland, 
with John Scumniotales and Jeff McKenna, developed a similar approach at Easel Corporation, and were the 
first to refer to it using the single word Scrum.
</p>

<h2>Sprint</h2>
<p>
A sprint is the basic unit of development in Scrum. Sprints last between one week and one month, and 
are a "timeboxed" (i.e. restricted to a specific duration) effort of a constant length. Each sprint is 
preceded by a planning meeting, where the tasks for the sprint are identified and an estimated 
commitment for the sprint goal is made, and followed by a review or retrospective meeting, where the 
progress is reviewed and lessons for the next sprint are identified. During each sprint, the team 
creates finished portions of a product. The set of features that go into a sprint come from the 
product backlog, which is a prioritized list of requirements. Which backlog items go into the sprint 
is determined during the sprint planning meeting. 
</p>

<p>During this meeting, the Product Owner informs the
team of the items in the product backlog that he or she wants completed (the ones with the highest 
priority). The team then determines how much of this they can commit to complete during the next 
sprint, and records this in the sprint backlog. During a sprint, no one is allowed to change the 
sprint backlog, which means that the requirements are frozen for that sprint. Development is timeboxed 
such that the sprint must end on time; if requirements are not completed for any reason they are left 
out and returned to the product backlog. After a sprint is completed, the team demonstrates how to 
use the software.
</p>

<h2>Roles</h2>

<p>
<b>Roles</b>&nbsp;
Scrum teams consist of three core roles and a range of ancillary roles core roles are often referred to as 
pigs and ancillary roles as chickens (after the story The Chicken and the Pig). The core roles in Scrum 
teams are those committed to the project in the Scrum process they are the ones producing the product 
(objective of the project).
</p>

<p>
<b>Product Owner</b>&nbsp;
The Product Owner represents the voice of the customer and is accountable for ensuring that the team delivers 
value to the business. The Product Owner writes customer-centric items (typically user stories), prioritizes 
them, and adds them to the product backlog. Scrum teams should have one Product Owner, and while they may 
also be a member of the development team, it is recommended that this role not be combined with that of 
Scrum Master.
</p>

<p>
<b>Development Team</b>&nbsp;
The Development Team is responsible for delivering potentially shippable product increments at the end of each 
Sprint. A Development Team is made up of 3 a 9 people with cross functional skills who do the actual work 
(analyse, design, develop, test, technical communication, document, etc.). The Development Team in Scrum is 
self-organizing, even though they may interface with project management organizations (PMOs).
</p>

<p>
<b>Scrum Master</b>&nbsp;
Scrum is facilitated by a Scrum Master, sometimes written as ScrumMaster, who is accountable for removing 
impediments to the ability of the team to deliver the sprint goal/deliverables. The Scrum Master is not the 
team leader, but acts as a buffer between the team and any distracting influences. The Scrum Master ensures 
that the Scrum process is used as intended. The Scrum Master is the enforcer of rules. A key part of the 
Scrum Master’s role is to protect the Development Team and keep it focused on the tasks at hand. The role
has also been referred to as a servant-leader to reinforce these dual perspectives. 
</p>
	

<h2>Burn Down Chart</h2>
<p>
The sprint burn down chart is a publicly displayed chart showing remaining work in the sprint backlog. 
Updated every day, it gives a simple view of the sprint progress. It also provides quick visualizations 
for reference. There are also other types of burndown, for example the release burndown chart that shows
the amount of work left to complete the target commitment for a Product Release (normally spanning 
through multiple iterations) and the alternative release burndown chart, which basically does the 
same, but clearly shows scope changes to Release Content, by resetting the baseline.
</p>';

